ll dp[20][10][2][2];
ll dfs(int n,string & s,int prev ,bool lead,bool tight){
    if(n==0)return 1;
    if(dp[n][prev][lead][tight]!=-1)return dp[n][prev][lead][tight];
    int ul=tight? s[s.size()-n]-'0':9;
    ll ans=0;
    for(int dig=0 ;dig<=ul;dig++){
        if(dig==prev && !lead){
            continue;
        }
        ans+= dfs(n-1,s,dig,lead&(dig==0),tight&(dig==ul));
    }
    return dp[n][prev][lead][tight]=ans;
}

void solve(){
    ll a,b;
    cin>>a>>b;
    string q=to_string(b);
    string p=to_string(a-1);
    memset(dp,-1,sizeof(dp));
    ll x=dfs(q.size(),q,-1,true,true);
    memset(dp,-1,sizeof(dp));
    ll y=dfs(p.size(),p,-1,true,true);
    cout<<x-y<<"\n";
}

